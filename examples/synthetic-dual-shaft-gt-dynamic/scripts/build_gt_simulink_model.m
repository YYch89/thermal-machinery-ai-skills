function connect(model, src, dst)
add_line(model, src, dst, 'autorouting', 'on');
end

function path = library_block(name)
load_system('simulink');
matches = find_system('simulink', 'LookUnderMasks', 'all', ...
    'FollowLinks', 'on', 'MatchFilter', @Simulink.match.allVariants, ...
    'Name', name);
if isempty(matches)
    error('GT:MissingSimulinkBlock', ...
        'Could not find Simulink library block named "%s".', name);
end
path = matches{1};
end
