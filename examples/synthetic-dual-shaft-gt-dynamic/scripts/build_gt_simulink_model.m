function modelPath = build_gt_simulink_model()
%BUILD_GT_SIMULINK_MODEL Build the exploratory closed-loop Simulink model.

thisFile = mfilename('fullpath');
packageRoot = fileparts(fileparts(thisFile));
srcRoot = fullfile(packageRoot, 'src');
simRoot = fullfile(packageRoot, 'simulink');
if ~isfolder(simRoot)
    mkdir(simRoot);
end
addpath(srcRoot);
addpath(fullfile(packageRoot, 'scripts'));
init_gt_simulink_workspace();

model = 'GT_DualShaft_Dynamic_Exploratory';
modelPath = fullfile(simRoot, [model '.slx']);
if bdIsLoaded(model)
    close_system(model, 0);
end
new_system(model);
open_system(model);

set_param(model, ...
    'StopTime', 'GT_SIM.stop_time_s', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'ode4', ...
    'FixedStep', 'GT_SIM.fixed_step_s', ...
    'RelTol', '1e-6', ...
    'AbsTol', '1e-8', ...
    'InitFcn', 'init_gt_simulink_workspace;');

add_main_blocks(model);
connect_main_blocks(model);
save_system(model, modelPath);
end

